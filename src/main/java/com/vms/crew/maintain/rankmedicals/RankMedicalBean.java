package com.vms.crew.maintain.rankmedicals;

public class RankMedicalBean {

	
	private String certificatename;
	private int rankcode;
	private int medicalcode;
	private String text1;
	private String id;
	private String text;
	
	public String getCertificatename() {
		return certificatename;
	}
	public void setCertificatename(String certificatename) {
		this.certificatename = certificatename;
	}
	public int getRankcode() {
		return rankcode;
	}
	public void setRankcode(int rankcode) {
		this.rankcode = rankcode;
	}
	public int getMedicalcode() {
		return medicalcode;
	}
	public void setMedicalcode(int medicalcode) {
		this.medicalcode = medicalcode;
	}
	public String getText1() {
		return text1;
	}
	public void setText1(String text1) {
		this.text1 = text1;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
}
