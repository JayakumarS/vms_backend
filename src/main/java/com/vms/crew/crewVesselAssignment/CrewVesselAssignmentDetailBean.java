package com.vms.crew.crewVesselAssignment;

public class CrewVesselAssignmentDetailBean {
	private String name;
	private String rank;
	private String signon;
	private String signoff;
	
	private String assignRemarks;
	private String voyageRemarks;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getSignon() {
		return signon;
	}
	public void setSignon(String signon) {
		this.signon = signon;
	}
	public String getSignoff() {
		return signoff;
	}
	public void setSignoff(String signoff) {
		this.signoff = signoff;
	}
	public String getAssignRemarks() {
		return assignRemarks;
	}
	public void setAssignRemarks(String assignRemarks) {
		this.assignRemarks = assignRemarks;
	}
	public String getVoyageRemarks() {
		return voyageRemarks;
	}
	public void setVoyageRemarks(String voyageRemarks) {
		this.voyageRemarks = voyageRemarks;
	}
	
	
	
}
